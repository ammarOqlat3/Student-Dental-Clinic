// 🔥 NEW: تحويل الأرقام للعربي
function convertToArabicNumbers(text) {
    return text.replace(/\d/g, d => '٠١٢٣٤٥٦٧٨٩'[d]);
}

function toggleLangMenu(e) {
    if (e) {
        e.preventDefault();
        e.stopPropagation();
    }
    const menu = document.getElementById("langMenuBox");
    if (!menu) return;

    if (menu.style.display === "flex") {
        menu.style.display = "none";
        document.removeEventListener("click", closeMenuOnce);
    } else {
        menu.style.display = "flex";
        setTimeout(() => {
            document.addEventListener("click", closeMenuOnce);
        }, 10);
    }
}

function closeMenuOnce(e) {
    const menu = document.getElementById("langMenuBox");
    if (menu && (!e || !e.target.closest(".lang-container"))) {
        menu.style.display = "none";
        document.removeEventListener("click", closeMenuOnce);
    }
}

function updateTextSafe(id, text) {
    const element = document.getElementById(id);
    if (element) {
        element.innerText = text;
    }
}

function applyTranslations(lang) {
    if (!translations[lang]) return;

    document.querySelectorAll("[data-key]").forEach(el => {
        const key = el.getAttribute("data-key");
        if (translations[lang][key]) {

            let value = translations[lang][key];

            // 🔥 NEW: تحويل الأرقام إذا عربي
            if (lang === "ar") {
                value = convertToArabicNumbers(value);
            }

            el.innerHTML = value;
        }
    });

    document.querySelectorAll("[data-key-placeholder]").forEach(el => {
        const key = el.getAttribute("data-key-placeholder");
        if (translations[lang][key]) {

            let value = translations[lang][key];

            // 🔥 NEW
            if (lang === "ar") {
                value = convertToArabicNumbers(value);
            }

            el.placeholder = value;
        }
    });
}

function changeLang(lang, e) {

    const menu = document.getElementById("langMenuBox");
    if (menu) menu.style.display = "none";
    document.removeEventListener("click", closeMenuOnce);

    if (e) {
        e.preventDefault();
        e.stopPropagation();
    }

    if (lang !== "ar" && lang !== "en") {
        lang = "en";
    }

    localStorage.setItem("lang", lang);

    updateTextSafe("currentLangDisplay", lang === "ar" ? "العربية" : "English");

    applyTranslations(lang);

    // 🔥 NEW: اتجاه + كلاس
    if (lang === "ar") {
        document.body.dir = "rtl";
        document.body.classList.add("rtl");
        document.body.classList.remove("ltr");
    } else {
        document.body.dir = "ltr";
        document.body.classList.add("ltr");
        document.body.classList.remove("rtl");
    }
}

window.onload = () => {
    let savedLang = localStorage.getItem("lang") || "en";
    changeLang(savedLang, null);

    const container = document.getElementById('container');
    const goRegisterBtn = document.getElementById('goRegister');
    const goLoginBtn = document.getElementById('goLogin');

    if (goRegisterBtn && container) {
        goRegisterBtn.addEventListener('click', () => container.classList.add('active'));
    }
    if (goLoginBtn && container) {
        goLoginBtn.addEventListener('click', () => container.classList.remove('active'));
    }
};