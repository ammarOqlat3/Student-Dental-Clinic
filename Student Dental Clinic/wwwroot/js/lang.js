// فتح/اغلاق القائمة
function toggleLangMenu(e) {
    // لمنع التضارب
    if (e) e.stopPropagation();
    else if (window.event) window.event.cancelBubble = true;

    const menu = document.getElementById("langMenu");
    if (!menu) return;

    if (menu.style.display === "block") {
        menu.style.display = "none";
        document.removeEventListener("click", closeMenuOnce);
    } else {
        menu.style.display = "block";
        // 🔥 تأخير بسيط جداً عشان ما يعتبر كبسة الفتح هي نفسها كبسة الإغلاق
        setTimeout(() => {
            document.addEventListener("click", closeMenuOnce);
        }, 10);
    }
}

function closeMenuOnce(e) {
    const menu = document.getElementById("langMenu");

    // إذا كبس برا القائمة
    if (menu && !e.target.closest(".lang-container")) {
        menu.style.display = "none";
        document.removeEventListener("click", closeMenuOnce);
    }
}

// دالة مساعدة لتغيير النص بدون ما يضرب الكود لو العنصر مش موجود
function updateTextSafe(id, text) {
    const element = document.getElementById(id);
    if (element) {
        element.innerText = text;
    }
}
// 🔥 دالة ترجمة عامة (لـ data-key)
function applyTranslations(lang) {
    document.querySelectorAll("[data-key]").forEach(el => {
        const key = el.getAttribute("data-key");

        if (translations[lang][key]) {
            el.textContent = translations[lang][key];
        }
    });

    // 🔥 إضافة مهمة (كنت ناسيها)
    document.querySelectorAll("[data-key-placeholder]").forEach(el => {
        const key = el.getAttribute("data-key-placeholder");

        if (translations[lang][key]) {
            el.placeholder = translations[lang][key];
        }
    });
}

// تغيير اللغة
function changeLang(lang, e) {
    if (e) e.stopPropagation();
    else if (window.event) window.event.cancelBubble = true;

    localStorage.setItem("lang", lang);

    // تغيير النصوص باستخدام الدالة الآمنة
    updateTextSafe("currentLang", lang === "ar" ? "العربية" : "English");
    updateTextSafe("siteTitle", translations[lang].title);
    updateTextSafe("navHome", translations[lang].home);
    updateTextSafe("navAbout", translations[lang].about);
    updateTextSafe("navService", translations[lang].service);
    updateTextSafe("navFeedback", translations[lang].feedback);
    updateTextSafe("loginText", translations[lang].login);

    // 🔥 ترجمة كل العناصر اللي فيها data-key
    applyTranslations(lang);

    document.body.dir = lang === "ar" ? "rtl" : "ltr";

    // 🔥 إغلاق القائمة (رح يوصلها الكود خاوة حتى لو في عناصر ناقصة)
    const menu = document.getElementById("langMenu");
    if (menu) {
        menu.style.display = "none";
    }

    // إيقاف مستمع الكبسات
    document.removeEventListener("click", closeMenuOnce);
}

// تحميل اللغة المحفوظة مباشرة
window.onload = () => {
    const savedLang = localStorage.getItem("lang") || "ar";
    changeLang(savedLang, null);
};